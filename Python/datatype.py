# Examples of strings

first_name = "Alice"
last_name = 'Johnson'
address = "123 Main Street"
phone = "555-1234" # This is text, not a number for math
message = "Hello there!"
empty_string = "" # An empty string (like an empty box)
# Why the quotes matter:
age_as_text = "25" # This is text - you can't do math with it
age_as_number = 25 # This is a number - you can do math with it

#printing all the variables

print(first_name, last_name,)
print(address,)
print(phone,)
print(message,)
print(empty_string,)
print(age_as_text,)
print(age_as_number,)

